using PMTs.DataAccess.Extentions;
using PMTs.DataAccess.Repository.Interfaces;
using PMTs.DataAccess.Shared;
using System;

namespace PMTs.DataAccess.Repository
{
    public class SendEmailAPIRepository : ISendEmailAPIRepository
    {
        private readonly string _actionName = "SendEmail";

        public SendEmailAPIRepository()
        {

        }

        public string GetEmailForSendNotifyByFactoryCode(string factoryCode, string token)
        {
            dynamic result = JsonExtentions.HttpActionToJwtPMTsApi(HTTPAction.GET.ToString(), Globals.WebAPIUrl + _actionName + "/GetEmailForSendNotifyByFactoryCode" + "?FactoryCode=" + factoryCode, string.Empty, token);

            if (result.Item1)
            {
                return Convert.ToString(result.Item3);
            }
            else
            {
                throw new Exception(result.Item2);
            }
        }


    }
}
